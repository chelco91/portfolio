					<h2>Introduction</h2>
					<p>The following document outlines the User Experience and Interaction Design for the proposed “Rochelle Lewis Personal Website Project”. This project is a professional portfolio website with a focus on front-end web development. This site is designed with a professional audience in mind, and is targeted to individuals within the web development and I.T. community nationwide. Target audience to include: startup founders, hiring managers, recruiters, and peers. The primary goal of this site is for users of the site to initiate contact in regards to employment and collaboration opportunities through the contact form, and/or the connected social media profiles. The secondary goal of this site is to showcase my work, and to contribute to a polished professional online presence.</p>
					<h2>Persona</h2>
					<p><strong>Name: </strong>David B. McCracken<br><strong>Age: </strong>34</p>
					<p><strong>Profession: </strong>Co-Founder and CTO of FashionGun.com, a startup that is looking to hire a full-time, in-house Front-End Web Developer. They have just received their first round of VC seed funding. Their product is an online collaboration and project management tool marketed to the fashion industry.</p>
					<p><strong>Technology: </strong>Mac power user. Uses a Mac Pro Desktop with a 30” cinema display while in the office, a MacBook Pro laptop at home, and an iPhone 6 with an unlimited 4G data connection.</p>
					<p><strong>Attitudes and Behaviors: </strong>Short on time, overworked, and has a full social schedule in addition. David will not spend more than 3 minutes max looking at anyone’s portfolio site, and only about 30 seconds on a resume. David is a huge social media user, and seeks potential candidates through tech and fashion community networking events, LinkedIn, and Twitter.</p>
					<p><strong>Frustrations and Needs: </strong>David needs to hire someone with top-tier skills who is willing to start at a mid-level salary. David is also looking for someone who is a good personality and culturural fit for FashionGun as well. FashionGun is still small, and can only offer limited benefits at this time, but will include equity options as part of the compensation package. Because David is looking to hire within a very competitive urban market, he is flooded with candidates but is having a hard time finding someone with the right combination of talent, experience, and personality who is willing to accept a lower salary and limited benefits. He must hire within 2 weeks.</p>
					<p><strong>Goals: </strong>David is only concerned with one thing: Is this person a potential candidate for the FashionGun Web Dev position? Can David, within 3 minutes max, determine whether this person is worthy of a preliminary phone screen regarding the job? David doesn’t like to waste time. David needs to know via a very fast interaction with the site whether or not this potential candidate has the minimum qualifications to warrant a call.</p>
					<h2>Use Case</h2>
					<p>David B. McCracken has received a cover letter and resume from myself in regards to the FashionGun Web Dev position. Based on a brief look at my resume package, he has included my name on a list of potential candidates to search for via LinkedIn.</p>
					<p>He has viewed my LinkedIn profile, and based on the number of connections we have in common, my work history, tweets and endorsements, he has decided to go one step further and check my personal web site for more information.</p>
					<p>It is 11:25AM on Monday morning, and David has a lunch appointment coming up in half an hour. David would like to get the first round of 10 preliminary phone screens finished between the hours of 1 - 3pm. Time is short, and all of this effort regarding hiring is additional work to be fit in on top of an already busy schedule. In addition to myself, he has 9 other candidates’ sites to look at.</p>
					<p>David is viewing the site on his desktop computer at the office, with a 30” cinema screen, using Safari. He has 29 other tabs open. From LinkedIn, he links to my personal website which opens in a new window. In the next 3 minutes, here is an outline of David’s interactions with the site:</p>
					<ol>
						<li>Lands on Home page. Clicks call to action that navigates to the Portfolio page.</li>
						<li>Views first three projects (3 of 8) in Portfolio gallery. Finds work satisfactory. Navigates to the “About Me” page via the main navigation in the header.</li>
						<li>1 minute look at the About Me page. Sees core skills featured in a contrasting side panel. The interaction stops here. David has decided, based on the information provided, that I will be included in the preliminary phone screens. He does not need to navigate away at this point, because my contact info is visible in the footer. David leaves the About Me page open for reference during the preliminary phone call later.</li>
					</ol>
