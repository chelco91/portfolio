					<h2>Proposed Site Map</h2>
					<p>Site will consist of 4 top-level pages total. No sub-pages or sub-navigation. This will be the Minimum Viable Product.</p>
					<ul>
						<li>Home Page</li>
						<li>About Me</li>
						<li>Portfolio</li>
						<li>Contact</li>
					</ul>
					<p><strong>Home Page: </strong>A brief introduction that clearly states my name and role as a Front End Developer, and will feature 2 calls to action that will direct the user to the About Me or the Portfolio page.</p>
					<p><strong>About Me: </strong>This page will detail my professional history, core skill set, and professional highlights. This page will showcase my strengths and personality. I will feature a contrasting sidebar that lists my core skills which will be easily readable at a glance. The tone of voice will be personable and casual, as I am only interested in opportunities with individuals and organizations that I am a good personality fit for. Depending on the length and detail of the content here, the Contact Form may also be incorporated here.</p>
					<p><strong>Portfolio: </strong>8 - 10 examples of my past work, including my roles and contributions to each project. I want this to be an animated, responsive image gallery.</p>
					<p><strong>Contact: </strong>This page will contain a simple contact form, my professional email address, phone number, and additional links to my professional social media profiles: LinkedIn, Twitter, GitHub, Dribbble. (These will also be linked to in the footer site wide via icons.) This page may be integrated into the About Me page if content is short.</p>
					<h2>Proposed Features and Functionality</h2>
					<ul>
						<li>Top navigation bar on all content pages, except for home page.</li>
						<li>Portfolio page with screenshots of projects, containing details overlaid on the image. This will use a jQuery gallery.</li>
						<li>Contact Form.</li>
						<li>Contact info and social media links to be featured in the footer on all pages.</li>
						<li>Iconography using Bootstrap included glyphs.</li>
						<li>Full size responsive background imagery. Will use jQuery for this.</li>
						<li>Contact info to be visible on each page in the Footer area.</li>
					</ul>
